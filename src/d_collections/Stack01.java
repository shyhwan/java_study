package d_collections;

import java.util.Stack;

public class Stack01 {
    public static void main(String[] args) {
        /* Stack
         * LIFO(Last in First Out)후입 선출 구조이다. 단방향 입출력 구조
         * 데이터를 하나씩만 넣고 뺼수 있다.
         */

        Stack<String> s = new Stack<>();

        s.push("null");

        s.pop();
    }
}
