package b_class;

public class Field {
    /*
     * Field
     * 클래스에 정의하는 속성은 객체의 상태에 따라 특정 값을 가지며, 객체의 상태를 표현한다.
     * 현실의 객체는 광법위한 속성들을 갖고 있기 때문에 정의하고자 하는 클래스의 특성을 잘 분석해 속성을 정의 한다.
     * 필드 정의시 반드시 Access Modifier(접근제어자), data type, field name을 명시 한다.
     * [static] [final] [접근제어자] [데이터 타입] [필드명(변수명)];
     * 
     */

    static class User {
        private String name;
        private int age;
    }
}
